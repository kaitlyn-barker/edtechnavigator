/**
 * Returns tools relevant to a given audience, sorted High → Medium → Low.
 * @param {"Teachers"|"Parents"|"Admins"} audience
 * @returns {Array}
 */
function getToolsForAudience(audience) {
  const relevanceKey = {
    Teachers: "teacherRelevant",
    Parents:  "parentRelevant",
    Admins:   "adminRelevant"
  }[audience];

  const order = { High: 0, Medium: 1, Low: 2 };

  return EDTECH_TOOLS
    .filter(t => order[t[relevanceKey]] <= 1) // High or Medium only
    .sort((a, b) => order[a[relevanceKey]] - order[b[relevanceKey]]);
}

/**
 * Filters a tool list by a search query across all relevant text fields.
 * @param {Array}  tools
 * @param {string} query
 * @returns {Array}
 */
function filterTools(tools, query) {
  if (!query || !query.trim()) return tools;
  const q = query.toLowerCase();
  return tools.filter(t =>
    t.name.toLowerCase().includes(q) ||
    t.vendor.toLowerCase().includes(q) ||
    t.category.toLowerCase().includes(q) ||
    t.subcategory.toLowerCase().includes(q) ||
    t.toolType.toLowerCase().includes(q) ||
    t.gradeLevels.toLowerCase().includes(q) ||
    t.platforms.toLowerCase().includes(q) ||
    t.subjects.some(s => s.toLowerCase().includes(q)) ||
    t.description.toLowerCase().includes(q) ||
    t.pricingModel.toLowerCase().includes(q)
  );
}
